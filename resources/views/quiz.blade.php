        // we run the function from two side 1 just add correct answer number and 2 will show answer
        function result(showAns) {
            var ul = document.getElementsByTagName('ul')[0];
            var li = ul.getElementsByTagName('li');
            if (showAns == false) {
                // it will add answer
                correctAns++;
